    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        u = dao.verificaLogin(txtLogin.getText(), txtSenha.getText());

        if (u != null) {
            if (u.getIdTipo().getIdtipoUsuario() == 1) {
                this.dispose();
                AdminGUI ag = new AdminGUI();
                ag.show();
            } else {
                if (u.getIdTipo().getIdtipoUsuario() == 2) {
                    this.dispose();
                    UsuarioGUI ug = new UsuarioGUI();
                    ug.show();
                } 
            }
        } else {
            lbErro.setVisible(true);
            lbErro.setText("Usuário Inválido!");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

